public class Coordinate {
    
    public int x;
    public int y;
    
    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    public void setCoordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    
}
